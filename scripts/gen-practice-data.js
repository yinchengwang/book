function serialize(items) {
  var parts = items.map(function (item) {
    var lines = "      {\n";
    lines += '        title: "' + item.title + '",\n';
    lines += '        platform: "' + item.platform + '",\n';
    lines +=
      "        problemId: " +
      (typeof item.problemId === "string"
        ? '"' + item.problemId + '"'
        : item.problemId) +
      ",\n";
    lines += '        difficulty: "' + item.difficulty + '",\n';
    lines += "        tags: " + JSON.stringify(item.tags) + ",\n";
    if (item.repeat) lines += "        repeat: true,\n";
    lines += "      }";
    return lines;
  });
  return parts.join(",\n");
}

