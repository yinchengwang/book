"use strict";
const fs = require("fs"),
  path = require("path");

var SRC = path.resolve(
  __dirname,
  "..",
  "project",
  "reading-radar",
  "data",
  "app",
  "practice-data.js",
);
var OUT = path.resolve(
  __dirname,
  "..",
  "project",
  "reading-radar",
  "data",
  "app",
  "practice",
);

var src = fs.readFileSync(SRC, "utf-8");
var fn = new Function(
  src.replace("const PRACTICE_CATEGORIES", "var PRACTICE_CATEGORIES") +
    "; return PRACTICE_CATEGORIES;",
);
var cats = fn();

// Create output directory
if (!fs.existsSync(OUT)) fs.mkdirSync(OUT, { recursive: true });

function serialize(items) {
  return (
    "[\n" +
    items
      .map(function (it) {
        var s = "    {\n";
        s += "      title: " + JSON.stringify(it.title) + ",\n";
        s += "      platform: " + JSON.stringify(it.platform) + ",\n";
        s +=
          "      problemId: " +
          (typeof it.problemId === "string"
            ? JSON.stringify(it.problemId)
            : it.problemId) +
          ",\n";
        s += "      difficulty: " + JSON.stringify(it.difficulty) + ",\n";
        s += "      tags: " + JSON.stringify(it.tags || []) + ",\n";
        if (it.repeat) s += "      repeat: true,\n";
        if (it.url) s += "      url: " + JSON.stringify(it.url) + ",\n";
        s += "    }";
        return s;
      })
      .join(",\n") +
    "\n  ]"
  );
}

cats.forEach(function (cat) {
  var body = '"use strict";\n\n';
  body += "(function(){\n";
  body += "  var C = window.PRACTICE_CATEGORIES;\n";
  body += "  if (!C) C = window.PRACTICE_CATEGORIES = [];\n";
  body += "  C.push({\n";
  body += "    id: " + JSON.stringify(cat.id) + ",\n";
  body += "    title: " + JSON.stringify(cat.title) + ",\n";
  body += "    phase: " + cat.phase + ",\n";
  body += "    order: " + cat.order + ",\n";
  body += "    icon: " + JSON.stringify(cat.icon) + ",\n";
  body += "    desc: " + JSON.stringify(cat.desc) + ",\n";
  body += "    items: " + serialize(cat.items) + ",\n";
  body += "  });\n";
  body += "})();\n";
  fs.writeFileSync(path.join(OUT, cat.id + ".js"), body, "utf-8");
  console.log(cat.id + ".js  (" + cat.items.length + " items)");
});

// Write index file (for reference, not used directly)
var idx = '"use strict";\n\n// Auto-generated by split-practice-data.js\n';
idx += "// Load order: phases 1→6, then by order within each phase\n\n";
idx += "var PRACTICE_CATEGORIES = [];\n";
cats.forEach(function (cat) {
  idx +=
    "// " +
    cat.phase +
    "." +
    cat.order +
    " " +
    cat.title +
    " (" +
    cat.items.length +
    " items)\n";
});
fs.writeFileSync(path.join(OUT, "_index.js"), idx, "utf-8");
console.log(
  "\nDone: " +
    cats.length +
    " files, " +
    cats.reduce(function (s, c) {
      return s + c.items.length;
    }, 0) +
    " items total",
);
