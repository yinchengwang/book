function serialize(arr) {
  var out = "[\n";
  arr.forEach(function (item, idx) {
    if (idx > 0) out += ",\n";
    out += "      {\n";
    out += "        title: " + JSON.stringify(item.title) + ",\n";
    out += "        platform: " + JSON.stringify(item.platform) + ",\n";
    out +=
      "        problemId: " +
      (typeof item.problemId === "string"
        ? JSON.stringify(item.problemId)
        : item.problemId) +
      ",\n";
    out += "        difficulty: " + JSON.stringify(item.difficulty) + ",\n";
    out += "        tags: " + JSON.stringify(item.tags) + ",\n";
    if (item.repeat) out += "        repeat: true,\n";
    out += "      }";
  });
  out += "\n    ]";
  return out;
}

