"use strict";

const fs = require("fs");
const path = require("path");

var DATA_FILE = path.resolve(
  __dirname,
  "..",
  "project",
  "reading-radar",
  "data",
  "app",
  "practice-data.js",
);

// ── 读取 ──
var src = fs.readFileSync(DATA_FILE, "utf-8");
var fixed = src.replace("const PRACTICE_CATEGORIES", "var PRACTICE_CATEGORIES");
var fn = new Function(fixed + "; return PRACTICE_CATEGORIES;");
var cats = fn();

console.log(
  "Read: " +
    cats.length +
    " categories, " +
    cats.reduce(function (s, c) {
      return s + c.items.length;
    }, 0) +
    " problems",
);

// ── 已存在题 ──
var existingSet = new Set();
cats.forEach(function (c) {
  c.items.forEach(function (item) {
    existingSet.add(item.platform + ":" + item.problemId);
  });
});

function lc(title, id, diff, tags) {
  return {
    title: String(title),
    platform: "leetcode",
    problemId: Number(id),
    difficulty: String(diff),
    tags: Array.isArray(tags) ? tags : [String(tags)],
  };
}
function jz(title, id, diff, tags) {
  return {
    title: "剑指 Offer " + id + ". " + title,
    platform: "nowcoder",
    problemId: String(id),
    difficulty: String(diff),
    tags: Array.isArray(tags) ? tags : [String(tags)],
  };
}

function byDiff(a, b) {
  return (a.difficulty === "easy" ? 0 : 1) - (b.difficulty === "easy" ? 0 : 1);
}

function addProblems(catId, newItems) {
  var cat = cats.filter(function (c) {
    return c.id === catId;
  })[0];
  if (!cat) {
    console.log("  WARN: category not found: " + catId);
    return;
  }
  var before = cat.items.length;
  newItems.forEach(function (item) {
    var key = item.platform + ":" + item.problemId;
    item.repeat = existingSet.has(key) ? true : undefined;
    existingSet.add(key);
    cat.items.push(item);
  });
  var seen = {};
  cat.items = cat.items.filter(function (item) {
    var key = item.platform + ":" + item.problemId;
    if (seen[key]) return false;
    seen[key] = true;
    return true;
  });
  cat.items.sort(byDiff);
  console.log(
    "  " +
      catId +
      ": " +
      before +
      " -> " +
      cat.items.length +
      " (+" +
      (cat.items.length - before) +
      ")",
  );
}

