const fs = require('fs');
const path = require('path');

// 简单的 PNG 文件头（1x1 像素透明 PNG）
// 这足够让 Taro 编译通过
const pngHeader = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  // IHDR chunk length + type
    0x00, 0x00, 0x00, 0x30, 0x00, 0x00, 0x00, 0x30,  // Width=48, Height=48
    0x08, 0x06, 0x00, 0x00, 0x00, 0x57, 0x02, 0xF9,  // Bit depth=8, Color type=6 (RGBA), etc
    0x87,                                              // CRC
    0x00, 0x00, 0x00, 0x1D, 0x49, 0x44, 0x41, 0x54,  // IDAT chunk
    0x78, 0x9C, 0xED, 0xC1, 0x01, 0x0D, 0x00, 0x00,
    0x00, 0xC2, 0xA0, 0xF7, 0x4F, 0x6D, 0x0E, 0x37,
    0xA0, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0xF8, 0x07, 0xF8,
    0x07,
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,  // IEND chunk
    0xAE, 0x42, 0x60, 0x82
]);

const assetsDir = path.join(__dirname, 'src', 'assets');
const icons = ['tab-home', 'tab-review', 'tab-tracker', 'tab-path', 'tab-settings'];

icons.forEach(icon => {
    fs.writeFileSync(path.join(assetsDir, `${icon}.png`), pngHeader);
    fs.writeFileSync(path.join(assetsDir, `${icon}-active.png`), pngHeader);
    console.log(`Created ${icon}.png and ${icon}-active.png`);
});

console.log('All icons created!');