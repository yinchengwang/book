/*
 * state_compression.h —— 状态压缩
 *
 * ============================================================
 * 概述
 * ============================================================
 * 状态压缩（State Compression）是一种用整数的二进制位表示集合或状态
 * 的技巧。常用于动态规划（DP）中将"已访问集合"压缩为一个整数，
 * 使 O(n!) 的暴力枚举降为 O(n·2^n) 的 DP。
 *
 * 核心思想：将 n 个元素的选择状态压缩为一个 n 位的二进制数。
 * 第 i 位为 1 表示元素 i 被选中/已访问。
 *
 * 常见位运算操作：
 * - 判断第 i 位是否为 1：  (mask >> i) & 1
 * - 将第 i 位置为 1：     mask | (1 << i)
 * - 将第 i 位置为 0：     mask & ~(1 << i)
 * - 翻转第 i 位：         mask ^ (1 << i)
 * - 获取最低位的 1：      mask & (-mask)
 * - 消去最低位的 1：      mask & (mask - 1)
 *
 * ============================================================
 * 经典应用
 * ============================================================
 *
 * 【旅行商问题 TSP（状态压缩 DP）】
 *   问题：给定 n 个城市和两两之间的距离，求从 0 出发访问所有城市
 *         恰好一次并回到 0 的最短路径。
 *
 *   状态定义：dp[mask][i] = 已访问城市集合为 mask，当前在城市 i 的最短距离。
 *   状态转移：dp[mask][i] = min(dp[mask\{i}][j] + dist[j][i])，其中 j 在 mask 中。
 *   复杂度：O(n²·2^n)，n ≤ 20 可行。
 *
 * 【N 皇后问题（位运算优化）】
 *   使用三个整数 col, diag1, diag2 分别表示列、左斜线、右斜线的占用状态，
 *   用位运算快速确定每行可放置皇后的位置。
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. 状态压缩 DP 的适用条件？→ n 很小（通常 ≤ 20），状态可以编码为位掩码。
 * 2. 如何枚举所有子集？→ for (sub = mask; sub; sub = (sub-1) & mask)。
 * 3. 状态压缩的优势和局限？→ 优势：压缩到 O(2^n)；局限：n 必须很小。
 */

