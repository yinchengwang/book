/*
 * hash_table.h —— 哈希表
 *
 * ============================================================
 * 概述
 * ============================================================
 * 哈希表（Hash Table，也叫散列表）是一种通过哈希函数将键（key）映射到
 * 存储位置的数据结构，理想情况下插入、查找、删除都是 O(1)。
 *
 * 核心组件：
 * 1. 哈希函数：将任意类型的 key 映射为一个整数（桶号）
 * 2. 桶数组：存储实际数据的数组，每个位置称为一个"桶"（bucket）
 * 3. 冲突解决策略：当两个不同的 key 映射到同一个桶时的处理方式
 *
 * 冲突解决策略对比：
 * - 拉链法（Chaining）：每个桶是一个链表，冲突时追加到链表尾部
 * - 开放寻址法（Open Addressing）：冲突时按某种规则探测下一个空位
 *   - 线性探测：hash(key) + 1, +2, +3, ...
 *   - 二次探测：hash(key) + 1^2, +2^2, +3^2, ...
 *   - 双重哈希：使用第二个哈希函数计算探测步长
 *
 * 本模块引用已有实现（src/algo/map/），该实现使用拉链法。
 *
 * ============================================================
 * 核心操作及时间复杂度
 * ============================================================
 * | 操作       | 平均复杂度 | 最坏复杂度    | 说明                         |
 * |-----------|-----------|-------------|-----------------------------|
 * | put       | O(1)      | O(n)        | 最坏情况：所有 key 冲突到同一桶  |
 * | get       | O(1)      | O(n)        |                             |
 * | remove    | O(1)      | O(n)        |                             |
 * | contains  | O(1)      | O(n)        |                             |
 * | size/empty| O(1)      | O(1)        |                             |
 *
 * ============================================================
 * 负载因子（Load Factor）与 rehash
 * ============================================================
 * 负载因子 α = n / m，其中 n 是元素数，m 是桶数。
 * - α 太小 → 浪费空间
 * - α 太大 → 冲突概率增加、性能下降
 * - 通常达到阈值（如 0.75）时触发 rehash：分配更大的桶数组，重新哈希所有元素
 *
 * ============================================================
 * 使用场景
 * ============================================================
 * - 缓存系统（LRU/LFU 的核心数据结构之一）
 * - 数据库索引（哈希索引）
 * - 字符串去重、词频统计
 * - 字典/符号表（编译器中的标识符查找）
 * - 任意 key-value 映射场景
 *
 * ============================================================
 * 代码示例
 * ============================================================
 *   #include <ds/hash_table.h>
 *   #include <stdio.h>
 *
 *   int main(void) {
 *       ds_hash_table_demo();
 *       return 0;
 *   }
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. HashMap vs TreeMap？→ HashMap O(1) 平均但不保证顺序；TreeMap O(log n)
 *    但保持有序遍历。
 * 2. 如何设计一个好的哈希函数？→ 均匀分布、计算快、确定性。常见方法：除留余数法、
 *    乘法哈希、针对特定类型的定制哈希。
 * 3. 哈希碰撞攻击？→ 攻击者构造大量碰撞的 key 使 CPU/内存飙升。防御：随机化种子、
 *    使用抗碰撞的哈希函数。
 * 4. resize 时为什么桶数通常取 2 的幂？→ 位运算取模 `hash & (n-1)` 比 `% n` 更快。
 */

