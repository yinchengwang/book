/*
 * heap.h —— 堆 / 优先队列
 *
 * ============================================================
 * 概述
 * ============================================================
 * 堆是一种特殊的完全二叉树，满足堆性质：
 * - 最大堆（Max-Heap）：每个节点的值 ≥ 其子节点的值（根节点最大）
 * - 最小堆（Min-Heap）：每个节点的值 ≤ 其子节点的值（根节点最小）
 *
 * 堆通常用数组实现（因为完全二叉树可以用数组紧凑存储）：
 *   对于下标 i 的节点：
 *   - 父节点：parent(i) = (i-1) / 2
 *   - 左子节点：left(i) = 2i + 1
 *   - 右子节点：right(i) = 2i + 2
 *
 * 数组表示示例（最小堆）：
 *         1           ← 根（最小）
 *       /   \
 *      3     6        ← 第二层
 *     / \   /
 *    5   9 8          ← 第三层
 *
 *   数组: [1, 3, 6, 5, 9, 8]
 *          0  1  2  3  4  5
 *
 * ============================================================
 * 核心操作及时间复杂度
 * ============================================================
 * | 操作     | 复杂度    | 说明                                |
 * |---------|----------|-------------------------------------|
 * | push    | O(log n) | 上浮（sift-up / bubble-up）          |
 * | pop     | O(log n) | 下沉（sift-down / bubble-down）      |
 * | top     | O(1)     | 查看堆顶（不移除）                     |
 * | heapify | O(n)     | 从无序数组构建堆，不是 O(n log n)！     |
 * | heap_sort| O(n log n) | 依次 pop 即可得到排序结果            |
 *
 * ============================================================
 * 使用场景
 * ============================================================
 * - 优先队列（CPU 调度、任务优先级管理）
 * - TopK 问题（维护大小为 K 的小顶堆）
 * - 堆排序（Heap Sort）
 * - Dijkstra 最短路径算法（贪心选择当前最短距离节点）
 * - 中位数查找（用两个堆——大顶堆+小顶堆维护数据流的中位数）
 *
 * ============================================================
 * 代码示例
 * ============================================================
 *   #include <ds/heap.h>
 *   #include <stdio.h>
 *
 *   int main(void) {
 *       ds_heap_demo();
 *       return 0;
 *   }
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. heapify 为什么是 O(n)？→ 因为大部分节点靠近底层，下沉距离短。
 *    精确分析：∑(h-i)·2^i = O(n)。
 * 2. 堆排序的步骤？→ ①建堆 O(n) ②依次弹出 O(n log n)，总体 O(n log n)，原地排序。
 * 3. TopK 用大顶堆还是小顶堆？→ 小顶堆！只保留 K 个最大元素，堆顶是这 K 个中最小的。
 *    新元素比堆顶大时，弹出堆顶，插入新元素。
 * 4. 优先队列的底层实现是什么？→ 二叉堆是最常用实现。还有二项堆、斐波那契堆等。
 */

