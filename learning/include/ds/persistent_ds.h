/*
 * persistent_ds.h —— 可持久化数据结构
 *
 * ============================================================
 * 概述
 * ============================================================
 * 可持久化数据结构（Persistent Data Structure）允许在修改数据结构后
 * 仍能访问其历史版本。与"可变"数据结构不同，可持久化数据结构不会
 * 覆盖旧数据，而是通过"路径复制"（Path Copying）创建新的节点来
 * 代表修改后的状态。
 *
 * 持久化级别：
 * - 部分持久化：可以查询任意历史版本，但只能修改最新版本
 * - 完全持久化：可以查询和修改任意历史版本（形成版本树）
 * - 融合持久化：可以合并两个版本（功能最强大）
 *
 * 本文件以"可持久化线段树"（主席树/Chairman Tree）为例，
 * 介绍路径复制思想。
 *
 * ============================================================
 * 路径复制原理
 * ============================================================
 *
 * 普通线段树的单点更新：直接修改路径上的节点（破坏旧版本）。
 *
 * 可持久化线段树：对于被修改的节点，创建新的"副本"节点，
 * 未修改的子树复用旧版本的节点。这样每次更新只需创建
 * O(log n) 个新节点，同时保留了历史版本。
 *
 * 示例：更新位置 2
 *
 *   旧版本 root0:              新版本 root1:
 *         A                          A'
 *       /   \                      /   \
 *      B     C        →          B'    C  (C 复用旧版本)
 *     / \   / \                 / \
 *    D  E  F  G               D  E' (D 复用旧版本)
 *        ↑                        ↑
 *      更新 E                    新 E' 包含更新的值
 *
 *   路径：A→B→E 被复制为新节点 A'→B'→E'，
 *   其他节点 C, D, F, G 在新旧版本间共享。
 *
 * ============================================================
 * 使用场景
 * ============================================================
 * - 区间第 K 小查询（主席树的经典应用）
 * - 函数式编程中的不可变数据结构
 * - Git 版本控制（commit 形成版本树）
 * - 时间旅行调试（Time-Travel Debugging）
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. 主席树为什么叫"主席树"？→ 由中国 OIer 黄嘉泰（网名 fotile96）发明，
 *    因其当时的头衔"主席"而得名。
 * 2. 可持久化的代价是什么？→ 每次更新创建 O(log n) 新节点，空间复杂度升高。
 * 3. 普通线段树能改造成可持久化吗？→ 能，只需将"原地修改"改为"新建节点 + 路径复制"。
 */

