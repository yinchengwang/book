/*
 * stack.h —— 栈
 *
 * ============================================================
 * 概述
 * ============================================================
 * 栈是一种后进先出（LIFO, Last-In-First-Out）的线性数据结构。
 * 元素只能从同一端（栈顶）插入和删除，就像叠盘子一样——最后放上去的
 * 最先被取下来。
 *
 * ============================================================
 * 核心操作及时间复杂度
 * ============================================================
 * | 操作       | 复杂度 | 说明                    |
 * |-----------|--------|------------------------|
 * | push      | O(1)*  | 入栈，均摊 O(1)         |
 * | pop       | O(1)   | 出栈                    |
 * | top       | O(1)   | 查看栈顶元素（不移除）    |
 * | size/empty| O(1)   |                        |
 *
 * ============================================================
 * 使用场景
 * ============================================================
 * - 函数调用栈（递归的底层机制）
 * - 括号匹配验证（经典面试题）
 * - 表达式求值（中缀转后缀、计算器）
 * - 浏览器的后退功能
 * - DFS（深度优先搜索）的非递归实现
 * - Undo（撤销）操作
 *
 * ============================================================
 * 代码示例
 * ============================================================
 *   #include <ds/stack.h>
 *   #include <stdio.h>
 *
 *   int main(void) {
 *       ds_stack_demo();
 *       return 0;
 *   }
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. 如何用两个栈实现队列？→ 入队时 push 到栈A；出队时若栈B为空，将栈A全部
 *    pop 并 push 到栈B，再从栈B pop。
 * 2. 如何实现最小栈（O(1) 获取最小值）？→ 维护两个栈：数据栈和最小值栈。
 * 3. 单调栈是什么？→ 栈内元素保持单调递增或递减，用于解决"下一个更大元素"类问题。
 */

