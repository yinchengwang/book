/*
 * linked_list.h —— 链表（学习层，独立实现）
 *
 * ============================================================
 * 概述
 * ============================================================
 * 链表是一种线性数据结构，由一系列节点组成，每个节点包含数据域和指向
 * 相邻节点的指针。与数组不同，链表的节点在内存中不必连续，因此插入和
 * 删除操作无需移动其他元素，但失去了 O(1) 随机访问的能力。
 *
 * 学习层迁移说明：本头文件原先位于工程层 engineering/include/ds/，
 * S5 续做时迁到学习层。原先依赖的 algo-prod/list/list.h 已被移除——
 * 学习层通过自身实现链表（linked_list.c）。
 */

/*
 * 链表的主要类型：
 * - 单向链表：每个节点只有 next 指针，只能单向遍历
 * - 双向链表：每个节点有 prev 和 next 指针，可双向遍历（本模块基于此实现）
 * - 循环链表：尾节点指向头节点，形成闭环
 *
 * ============================================================
 * 核心操作及时间复杂度
 * ============================================================
 * | 操作              | 平均复杂度 | 最坏复杂度 | 说明                     |
 * |-------------------|-----------|-----------|-------------------------|
 * | push_front/back   | O(1)      | O(1)      | 头/尾插入                |
 * | pop_front/back    | O(1)      | O(1)      | 头/尾删除                |
 * | front/back        | O(1)      | O(1)      | 获取首/尾元素             |
 * | 按值查找           | O(n)      | O(n)      | 需要遍历                  |
 * | 按索引访问         | O(n)      | O(n)      | 从最近端遍历到目标位置     |
 * | size / empty      | O(1)      | O(1)      |                          |
 *
 * ============================================================
 * 与数组的对比
 * ============================================================
 * | 维度       | 动态数组 (ds_array)  | 双向链表 (linked_list) |
 * |-----------|---------------------|----------------------|
 * | 随机访问    | O(1)                | O(n)                 |
 * | 头插/头删  | O(n)                | O(1)                 |
 * | 尾插/尾删  | O(1) 均摊            | O(1)                 |
 * | 内存布局    | 连续                 | 分散，每节点有指针开销  |
 * | 缓存友好性  | 好                   | 差                    |
 * | 迭代器失效  | 扩容时全部失效        | 仅被删除节点失效        |
 *
 * ============================================================
 * 使用场景
 * ============================================================
 * - 需要频繁在头部增删元素（如 LRU 缓存的内部结构）
 * - 插入/删除操作远多于随机访问
 * - 需要稳定的迭代器（插入不会使指向其他元素的指针失效）
 *
 * ============================================================
 * 代码示例
 * ============================================================
 *   #include <ds/linked_list.h>
 *   #include <stdio.h>
 *
 *   int main(void) {
 *       ds_linked_list_demo();
 *       return 0;
 *   }
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. 如何判断链表有环？→ 快慢指针（Floyd 判圈算法）。
 * 2. 如何找到链表中间节点？→ 快慢指针，快指针每次两步，慢指针一步。
 * 3. 如何反转链表？→ 遍历时逐个修改 next 指向，需要 prev/curr/next 三指针。
 * 4. 链表 vs 数组的实际选择依据？→ 关注操作模式（头/尾增删？随机访问？）和缓存性能。
 */

