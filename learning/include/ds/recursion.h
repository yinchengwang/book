/*
 * recursion.h —— 递归结构
 *
 * ============================================================
 * 概述
 * ============================================================
 * 递归是一种编程技巧，函数直接或间接地调用自身来解决问题。
 * 虽然不是一种"数据结构"，但递归是理解树、图等递归定义的数据结构的基础。
 *
 * 递归三要素：
 * 1. 终止条件（Base Case）：问题规模最小时的直接答案
 * 2. 递归关系（Recursive Case）：将大问题分解为一个或多个更小的同类子问题
 * 3. 返回值合并：将子问题的解组合成原问题的解
 *
 * 递归 vs 迭代：
 * - 递归：代码简洁优雅，但可能有栈溢出风险和函数调用开销
 * - 迭代：通常更高效，但复杂问题（如树的遍历）的迭代写法较复杂
 *
 * ============================================================
 * 常见递归模式
 * ============================================================
 * - 分治（Divide & Conquer）：将问题分解为多个独立子问题（归并排序、快速排序）
 * - 回溯（Backtracking）：尝试所有可能，失败则撤销选择（N 皇后、排列组合）
 * - 树/图遍历：前序、中序、后序、DFS
 * - 动态规划（记忆化递归）：递归 + 缓存中间结果（斐波那契优化）
 *
 * ============================================================
 * 代码示例
 * ============================================================
 *   #include <ds/recursion.h>
 *   #include <stdio.h>
 *
 *   int main(void) {
 *       ds_recursion_demo();
 *       return 0;
 *   }
 *
 * ============================================================
 * 面试常见问题
 * ============================================================
 * 1. 递归容易出现什么问题？→ 栈溢出（无终止条件或递归深度过大）、重复计算。
 * 2. 如何优化递归？→ 记忆化搜索（缓存）、尾递归优化（编译器）、转为迭代。
 * 3. 什么场景应该用递归？→ 问题本身具有自相似结构（树、图、分治）。
 * 4. 尾递归是什么？→ 递归调用是函数的最后一步操作，编译器可优化为循环。
 */

